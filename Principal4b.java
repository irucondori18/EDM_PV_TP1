package ar.edu.unju.edm.principal;

public class Principal4b {

	public static void main(String[] args) {

		for (int a=0,b=0; a < 7; a++,b+=2){
			System.out.println("valor de a: "+a);
			System.out.println("valor de b: "+b);
		}
		
		System.out.println();
		
		int a = 0;
		int b = 0;
		while(a < 7){
			System.out.println("valor de a: "+a);
			System.out.println("valor de b: "+b);
			a++;
			b+=2;
		}
		
	}

}
